#!/bin/bash
set -e

echo "=========================================================="
echo " ЗАПУСК АВТОМАТИЧЕСКОГО ТЕСТИРОВАНИЯ NVIDIA TESLA V100    "
echo "=========================================================="

# 1. Проверяем, видит ли контейнер драйвер и видеокарты
if ! command -v nvidia-smi &> /dev/null; then
    echo "Ошибка: утилита nvidia-smi не найдена. Проверьте nvidia-container-toolkit на хосте."
    exit 1
fi

echo "Обнаруженные видеокарты в системе:"
nvidia-smi --query-gpu=index,name,serial,uuid --format=csv

# 2. Запускаем службу мониторинга DCGM (обязательно для работы dcgmi)
echo -e "\nЗапуск фоновой службы nv-hostengine..."
nv-hostengine

# Даем службе 2 секунды на инициализацию
sleep 2

# 3. Запуск комплексной диагностики (Level 3 - Стресс-тест памяти, PCIe шины и ядер)
# Параметр -r 3 запускает полный диагностический пакет (включая проверку на ошибки памяти)
echo -e "\nЗапуск глубокого диагностического теста DCGM (Level 3)..."
echo "Это займет несколько минут. Пожалуйста, подождите..."
echo "----------------------------------------------------------"

# Выполняем тест для всех найденных GPU (-a)
set +e
dcgmi diag -r 3 -a > /tmp/dcgm_report.txt
DIAG_EXIT_CODE=$?
set -e

# 4. Вывод отчета в консоль
echo "----------------------------------------------------------"
echo "                      ОТЧЕТ ТЕСТИРОВАНИЯ                  "
echo "----------------------------------------------------------"
cat /tmp/dcgm_report.txt

# 5. Оценка результатов
echo "----------------------------------------------------------"
if [ $DIAG_EXIT_CODE -eq 0 ]; then
    echo "РЕЗУЛЬТАТ: Все видеокарты успешно прошли тестирование!"
    exit 0
else
    echo "РЕЗУЛЬТАТ: Обнаружены проблемы или ошибки в работе GPU."
    exit $DIAG_EXIT_CODE
fi
